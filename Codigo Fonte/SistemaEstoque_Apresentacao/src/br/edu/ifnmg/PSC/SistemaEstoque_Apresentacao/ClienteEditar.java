    @Override
    public void carregaCampos() {
        txtNome.setText(entidade.getNomeCompleto());
        txtEndereco.setText(entidade.getEndereco());
        txtCpf.setText(entidade.getCpf());
        txtTelefone.setText(entidade.getTelefone());
        txtDataNasc.setValue(entidade.getDataNascimento());
        
    }

    @Override
    public void carregaObjeto() throws RegraNegocioException {
         try {
           
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            entidade.setNomeCompleto(txtNome.getText());
            entidade.setEndereco(txtEndereco.getText());
            entidade.setCpf(txtCpf.getText());
            entidade.setTelefone(txtTelefone.getText());
            entidade.setDataNascimento(df.parse(txtDataNasc.getText()));
   
         } catch (ParseException ex) {
            Logger.getLogger(ProdutoEditar.class.getName()).log(Level.SEVERE, null, ex);
         }
    }

    @Override
    public boolean verificarCamposObrigatorios() {
         if(txtNome.getText().length() ==0 || txtNome.getText().length() ==0 || 
                 txtTelefone.getText().length() ==0 || 
                 txtCpf.getText().length() ==0 || 
                 txtEndereco.getText().length() ==0 || txtDataNasc.getValue() == null)
            return false;
        return true;
    
    }
